#include "libft.h"

size_t	ft_strlcat(char *restrict dst, const char *restrict src, size_t dstsize)
{
	size_t	ret;

	ret = ft_strlcpy(dst + ft_strlen(dst), src, dstsize);
	return (ret);
}
